// switch
var color = 'white';
switch (color) {
    case 'black':
        console.log('this is black color')
        break;
    case 'blue':
        console.log('this is blue color')
        break;
    case 'yellow':
        console.log('this is yellow color')
        break;
    case 'white':
        console.log('this is white color')
        break;

    default:
        console.log('this color is nothing')
        break;
}